
class Vector7D:
    def __init__(self, x: float, y: float, z: float, x_axis: float, y_axis: float, z_axis: float, rotation: float):
        self.x = x
        self.y = y
        self.z = z
        self.x_axis = x_axis
        self.y_axis = y_axis
        self.z_axis = z_axis
        self.rotation = rotation